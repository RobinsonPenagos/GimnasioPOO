public class Cliente {
    private String nombre;
    private int edad;
    private int meses;

    public Cliente(String nombre, int edad, int meses) {
        this.nombre = nombre;
        this.edad = edad;
        this.meses = meses;
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public int getMeses() {
        return meses;
    }
}
    

